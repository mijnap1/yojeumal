const pixelsPerDay = 3.15;
const trackWidth = Math.round((end - start) / day * pixelsPerDay);
// 밀집 구간에서도 아직 사용 중인 줄을 강제로 재사용하지 않도록 여유 줄을 둔다.
const laneLastRight = Array(12).fill(-Infinity);
const minimumLabelGap = 28;

track.style.setProperty("--track-width", `${trackWidth}px`);

function xFor(date) {
  return ((new Date(`${date}T00:00:00`) - start) / day) * pixelsPerDay;
}

function estimatedLabelWidth(term) {
  const textWidth = Array.from(term).reduce((width, character) => {
    return width + (/[ㄱ-힝]/.test(character) ? 22 : 12);
  }, 0);
  const iconWidth = iconPaths[term] ? 30 : 0;
  return Math.max(88, textWidth + iconWidth);
}

for (let year = 2020; year <= 2026; year += 1) {
  const mark = document.createElement("span");
  mark.className = "year-mark";
  mark.textContent = year;
  mark.style.setProperty("--x", `${xFor(`${year}-01-01`)}px`);
  track.append(mark);
}

trends.forEach(([date, term], index) => {
  const x = xFor(date);
  const labelWidth = estimatedLabelWidth(term);
  const labelLeft = x - labelWidth / 2;
  let lane = laneLastRight.findIndex((lastRight) => labelLeft - lastRight >= minimumLabelGap);
  if (lane === -1) lane = index % laneLastRight.length;
  laneLastRight[lane] = x + labelWidth / 2;

  const event = document.createElement("article");
  const side = lane % 2 === 0 ? "up" : "down";
  const depth = Math.floor(lane / 2);
  event.className = `event ${side}`;
  event.tabIndex = 0;
  event.setAttribute("role", "button");
  event.setAttribute("aria-pressed", "false");
  event.style.setProperty("--x", `${x}px`);
  event.style.setProperty("--lane", depth);
  event.setAttribute("aria-label", `${date}, ${term}`);
  event.innerHTML = `
    <div class="event-card">
      <span class="term-row">${renderIcon(term)}<span class="term">${term}</span></span>
      <time class="date" datetime="${date}">${date.replaceAll("-", ".")}</time>
    </div>`;
  event.addEventListener("click", (clickEvent) => {
    clickEvent.stopPropagation();
    selectEvent(event, date, term);
  });
  event.addEventListener("keydown", (keyEvent) => {
    if (keyEvent.key === "Enter" || keyEvent.key === " ") {
      keyEvent.preventDefault();
      selectEvent(event, date, term);
    }
  });
  track.append(event);
});

function closeDefinition() {
  if (selectedEvent) {
    selectedEvent.classList.remove("selected");
    selectedEvent.setAttribute("aria-pressed", "false");
  }
  selectedEvent = null;
  definitionPanel.classList.remove("open", "is-flipped");
  definitionPanel.setAttribute("aria-hidden", "true");
}

function setDefinitionSide(showMedia) {
  const isFlipped = Boolean(showMedia && definitionPanel.classList.contains("has-media"));
  definitionPanel.classList.toggle("is-flipped", isFlipped);
  definitionCopy.setAttribute("aria-hidden", String(isFlipped));
  definitionMedia.setAttribute("aria-hidden", String(!isFlipped));
  definitionFlip.setAttribute("aria-label", isFlipped ? "설명으로 돌아가기" : "이미지 보기");
}

function selectEvent(event, date, term) {
  if (selectedEvent === event) {
    closeDefinition();
    return;
  }

  if (selectedEvent) {
    selectedEvent.classList.remove("selected");
    selectedEvent.setAttribute("aria-pressed", "false");
  }

  selectedEvent = event;
  event.classList.add("selected");
  event.setAttribute("aria-pressed", "true");
  definitionTerm.textContent = term;
  definitionIcon.innerHTML = renderIcon(term);
  definitionIcon.classList.toggle("visible", Boolean(iconPaths[term]));
  definitionDate.textContent = date.replaceAll("-", ".");
  definitionDate.dateTime = date;
  definitionText.textContent = toPlainStyle(definitions[term] ?? "이 시기에 온라인과 일상에서 반복적으로 사용된 유행어다.");
  definitionOriginText.textContent = origins[term] ?? "온라인에서 반복적으로 변형·사용되며 퍼졌다. 명확한 최초 창작자는 알려지지 않았다.";
  const [when, example] = usageDetails[term] ?? ["뜻과 맞는 상황에서 반응이나 문장으로 활용할 때", `“${term}”`];
  definitionWhen.textContent = when;
  definitionExample.textContent = `예: ${example}`;
  const media = mediaDetails[term];
  definitionPanel.classList.toggle("has-media", Boolean(media));
  definitionFlip.disabled = !media;
  definitionFlip.setAttribute("aria-hidden", String(!media));
  setDefinitionSide(false);
  if (media) {
    definitionMediaImage.src = media.src;
    definitionMediaImage.alt = media.alt;
    definitionMediaCaption.textContent = media.caption;
  } else {
    definitionMediaImage.removeAttribute("src");
    definitionMediaImage.alt = "";
    definitionMediaCaption.textContent = "";
  }
  definitionPanel.classList.add("open");
  definitionPanel.setAttribute("aria-hidden", "false");
}

function toPlainStyle(text) {
  return text
    .replaceAll("이에요.", "이다.")
    .replaceAll("예요.", "이다.")
    .replaceAll("해요.", "한다.")
    .replaceAll("돼요.", "된다.")
    .replaceAll("써요.", "쓴다.")
    .replaceAll("뜻해요.", "뜻한다.")
    .replaceAll("사용해요.", "사용한다.");
}

